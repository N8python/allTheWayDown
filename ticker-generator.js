    weightedRandom(weights) {
        let total = Object.values(weights).reduce((a, b) => a + b, 0);
        let r = Math.random() * total;
        let cumulative = 0;

        for (let [item, weight] of Object.entries(weights)) {
            cumulative += weight;
            if (r <= cumulative) return item;
        }

        return Object.keys(weights)[0];
    }

    generateTicker(minLength = 3) {
        // Start with first character
        let ticker = this.weightedRandom(this.startTokens);
        let current = ticker;

        // Generate remaining characters
        const maxLength = minLength + Math.floor(Math.random() * 3);
        while (ticker.length < maxLength) {
            if (!this.transitions[current]) break;

            const next = this.weightedRandom(this.transitions[current]);
            ticker += next;
            current = next;

            // Random chance to end if we're past minimum length
            if (ticker.length >= minLength && Math.random() < 0.3) break;
        }

        // If too short, try again
        if (ticker.length < minLength) {
            return this.generateTicker(minLength, maxLength);
        }

        return ticker.toUpperCase();
    }

    generateMany(count = 20, maxAttempts = 40) {
        const generated = new Set();
        let attempts = 0;

        while (generated.size < count && attempts < maxAttempts) {
            const ticker = this.generateTicker();
            generated.add(ticker);
            attempts++;
        }

        return Array.from(generated).sort();
    }
